#include <iostream>

using namespace std;

int main()
{

    cout << "(10 > 12)  = " << (10 > 12) << endl;
    cout << "(10 < 12)  = " << (10 < 12) << endl;
    cout << "(10 >= 10) = " << (10 >= 10) << endl;
    cout << "(10 <= 11) = " << (10 <= 11) << endl;
    cout << "(10 == 12) = " << (10 == 12) << endl;
    cout << "(12 == 12) = " << (12 == 12) << endl;
    cout << "(10 != 12) = " << (10 != 12) << endl;

    return 0;
}
