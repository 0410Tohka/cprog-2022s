#include <iostream>

using namespace std;

int main()
{
    int a, b;

    a = 123;
    b = 321;

    cout<<"a = "<<a<<endl;
    cout<<"b = "<<b<<endl;
    cout<<"(a + b) = "<<(a + b)<<endl;
    cout<<"(a - b) = "<<(a - b)<<endl;
    cout<<"(a * b) = "<<(a * b)<<endl;
    cout<<"(a / b) = "<<(a / b)<<endl;
    cout<<"(a % 8) = "<<(a % 8)<<endl;


    return 0;
}
